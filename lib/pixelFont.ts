/* ============================================
   Pixel Font — bitmap definitions for title characters
   Grid: width columns × 7 rows per character
   ============================================ */

export interface PixelChar {
  width: number;
  height: number;
  /** [col, row] pairs for each active pixel */
  pixels: [number, number][];
}

export interface RectData {
  x: number;
  y: number;
  w: number;
  h: number;
  cx: number;
  cy: number;
}

export function buildLayout(text: string, pixelUnit = 0.7, charGap = 1): {
  rects: RectData[];
  totalCols: number;
  totalRows: number;
} {
  const rects: RectData[] = [];
  let col = 0;
  const totalRows = 7;

  for (const char of text) {
    const def = PIXEL_CHARS[char];
    if (!def) {
      col += 3 + charGap;
      continue;
    }

    for (const [px, py] of def.pixels) {
      const x = (col + px) * pixelUnit;
      const y = py * pixelUnit;
      rects.push({
        x,
        y,
        w: pixelUnit,
        h: pixelUnit,
        cx: x + pixelUnit / 2,
        cy: y + pixelUnit / 2,
      });
    }
    col += def.width + charGap;
  }

  return { rects, totalCols: col - charGap, totalRows };
}

